    {
        $payroll=HRMmanagement::select('leavedetails.*','h_r_mmanagements.Employeename','h_r_mmanagements.Basicsalary')
            ->join('leavedetails','leavedetails.empid','=','h_r_mmanagements.id')
            ->where('leavedetails.empid','=',$id)
            ->first();
        $pdf=PDF::loadView('directors.show_pdf',compact('payroll'));

        return $pdf->stream('Salary Details.pdf');
    }

